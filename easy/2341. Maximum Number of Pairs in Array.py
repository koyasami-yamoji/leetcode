import collections

from typing import List



def numberOfPairs(nums: List[int]) -> List[int]:
	pair = 0
	resume = 0
	for i in collections.Counter(nums).values():
		pair += i // 2
		resume += i % 2
	return [pair, resume]

print(numberOfPairs([1,3,2,1,3,2,2]))


